static const std::vector<uint8_t>&
GetExpectedDigest(const TestVector& tv, HashAlgorithm algo) {
    switch (algo) {
        case HashAlgorithmMD4:    return tv.md4;
        case HashAlgorithmMD5:    return tv.md5;
        case HashAlgorithmSHA1:   return tv.sha1;
        case HashAlgorithmSHA256: return tv.sha256;
        case HashAlgorithmSHA384: return tv.sha384;
        case HashAlgorithmSHA512: return tv.sha512;
        case HashAlgorithmNTLM:   return tv.ntlm;
        default: abort();
    }
}

static std::string ToHex(const uint8_t* buf, size_t len) {
    std::string s;
    s.reserve(len * 2);
    for (size_t i = 0; i < len; i++) {
        char h[3];
        snprintf(h, sizeof(h), "%02x", buf[i]);
        s += h;
    }
    return s;
}

// ============================================================
// Parameterized test: algorithm × vector
// ============================================================

struct FunctionalParam {
    HashAlgorithm algo;
    size_t vectorIndex;
};

class FunctionalTest : public ::testing::TestWithParam<FunctionalParam> {};

static std::string FunctionalTestName(const ::testing::TestParamInfo<FunctionalParam>& info) {
    std::string name = HashAlgorithmToString(info.param.algo);
    name += "_vec" + std::to_string(info.param.vectorIndex);
    name += "_len" + std::to_string(kTestVectors[info.param.vectorIndex].preimage.size());
    return name;
}

TEST_P(FunctionalTest, SingleHash) {
    auto [algo, idx] = GetParam();
    const auto& tv = kTestVectors[idx];
    const auto& expected = GetExpectedDigest(tv, algo);
    size_t digestLen = expected.size();

    uint8_t hash[MAX_HASH_SIZE];
    SimdHashSingle(algo, tv.preimage.size(),
                   (const uint8_t*)tv.preimage.data(), hash);
    EXPECT_EQ(0, memcmp(hash, expected.data(), digestLen))
        << "Expected: " << ToHex(expected.data(), digestLen)
        << "\n  Actual: " << ToHex(hash, digestLen);
}

TEST_P(FunctionalTest, SimdHashAllSameLane) {
    auto [algo, idx] = GetParam();
    const auto& tv = kTestVectors[idx];
    const auto& expected = GetExpectedDigest(tv, algo);
    size_t digestLen = expected.size();
    size_t lanes = SimdLanes();

    const uint8_t* buffers[MAX_LANES];
    size_t lengths[MAX_LANES];
    for (size_t i = 0; i < lanes; i++) {
        buffers[i] = (const uint8_t*)tv.preimage.data();
        lengths[i] = tv.preimage.size();
    }

    uint8_t hashes[MAX_LANES * MAX_HASH_SIZE];
    SimdHash(algo, lengths, buffers, hashes);

    for (size_t i = 0; i < lanes; i++) {
        EXPECT_EQ(0, memcmp(&hashes[i * digestLen], expected.data(), digestLen))
            << "Lane " << i
            << "\nExpected: " << ToHex(expected.data(), digestLen)
            << "\n  Actual: " << ToHex(&hashes[i * digestLen], digestLen);
    }
}

// Generate all (algorithm, vector) combinations
static std::vector<FunctionalParam> GenerateParams() {
    std::vector<FunctionalParam> params;
    HashAlgorithm algos[] = {
        HashAlgorithmMD4, HashAlgorithmMD5, HashAlgorithmSHA1,
        HashAlgorithmSHA256, HashAlgorithmSHA384, HashAlgorithmSHA512,
        HashAlgorithmNTLM
    };
    for (auto algo : algos) {
        for (size_t i = 0; i < kTestVectors.size(); i++) {
            params.push_back({algo, i});
        }
    }
    return params;
}

INSTANTIATE_TEST_SUITE_P(
    Functional, FunctionalTest,
    ::testing::ValuesIn(GenerateParams()),
    FunctionalTestName
);

