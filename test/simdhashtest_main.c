static const size_t VectorCount = sizeof(SimdHashTestVectors)/sizeof(TestVector);

static bool
ToHex(char* Out, size_t OutLength, const uint8_t* Buffer, size_t BufferLength)
{
	char* nextOut;
	
	if (OutLength < BufferLength * 2)
	{
		return false;
	}
	
	nextOut = Out;
	for (size_t i = 0; i < BufferLength; i++)
	{
		snprintf(nextOut, 3, "%02x", Buffer[i]);
		nextOut += 2;
	}
	
	return true;
}

static void
GetTestVector(
	const size_t Index,
	const HashAlgorithm Algorithm,
	uint8_t** PreImage,
	size_t* PreImageLength,
	uint8_t** Digest,
	size_t* DigestLength
)
{
	*PreImage = NULL;
	*PreImageLength = 0;
	*Digest = NULL;
	*DigestLength = 0;

	if (Index >= VectorCount)
	{
		return;
	}

	*PreImage = SimdHashTestVectors[Index].PreImage;
	*PreImageLength = SimdHashTestVectors[Index].Length;

	switch (Algorithm)
	{
	case HashAlgorithmMD4:
		*Digest = (uint8_t*)SimdHashTestVectors[Index].Md4Digest;
		*DigestLength = MD4_SIZE;
		break;
	case HashAlgorithmMD5:
		*Digest = (uint8_t*)SimdHashTestVectors[Index].Md5Digest;
		*DigestLength = MD5_SIZE;
		break;
	case HashAlgorithmSHA1:
		*Digest = (uint8_t*)SimdHashTestVectors[Index].Sha1Digest;
		*DigestLength = SHA1_SIZE;
		break;
	case HashAlgorithmSHA256:
		*Digest = (uint8_t*)SimdHashTestVectors[Index].Sha256Digest;
		*DigestLength = SHA256_SIZE;
		break;
	case HashAlgorithmSHA384:
		*Digest = (uint8_t*)SimdHashTestVectors[Index].Sha384Digest;
		*DigestLength = SHA384_SIZE;
		break;
	case HashAlgorithmSHA512:
		*Digest = (uint8_t*)SimdHashTestVectors[Index].Sha512Digest;
		*DigestLength = SHA512_SIZE;
		break;
	case HashAlgorithmNTLM:
		*Digest = (uint8_t*)SimdHashTestVectors[Index].NtlmDigest;
		*DigestLength = MD4_SIZE;
		break;
	default:
		return;
	}
}

